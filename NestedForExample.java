public class NestedForExample {
    public static void main(String[] args) {
        System.out.println("K039/Zaid Mukaddam");
        // loop of i
        for (int i = 1; i <= 3; i++) {
            // loop of j
            for (int j = 1; j <= 3; j++) {
                System.out.println(i + " " + j);
            } // end of i
        } // end of j
    }
}