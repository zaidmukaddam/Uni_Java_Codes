class Main {
    public static void main(String[] args) {

        int a = 12, b = 5,c=20,d=11;

        System.out.println("a+b = " + (a + b));

        System.out.println("a-b = " + (a - b));

        System.out.println("a*b = " + (a * b));

        System.out.println("a/b = " + (a / b));

        System.out.println("c%b = " + (c % b));

        System.out.println("a++ = " + (a++));

        System.out.println("a-- = " + (a--));

        System.out.println("d++ = " + (d++));

        System.out.println("++d = " + (++d));

    }
}